foods = ['bacon', 'tuna', 'ham', 'sausages', 'rice']

"""
Creating a for loop that runs through the list 'foods' and treats them as variable 'f' and
prints that list in order.
"""

# for each item in the foods list
# then print the length 'len' of that item
# also you can slice lists by using ':2' or whatever spots you want to choose
# in square brackets
for f in foods[:2]:
    print(f)
    print(len(f))
